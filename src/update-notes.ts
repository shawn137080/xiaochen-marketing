async function main() {
  console.log("更新笔记内容和标签...\n");

  for (const u of updates) {
    const note = await db.xhsNote.findFirst({ where: { title: u.title } });
    if (!note) {
      console.log(`❌ 找不到笔记: ${u.title}`);
      continue;
    }

    await db.xhsNote.update({
      where: { id: note.id },
      data: {
        content: u.content,
        tags: JSON.stringify(u.tags),
      },
    });

    console.log(`✅ 已更新: ${u.title}`);
    console.log(`   标签(${u.tags.length}个): ${u.tags.join(", ")}\n`);
  }

  console.log("🎉 完成！");
}

main()
  .then(() => process.exit(0))
  .catch((e) => { console.error(e); process.exit(1); });
