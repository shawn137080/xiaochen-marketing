async function main() {
  let updated = 0;
  for (const [oldTitle, data] of Object.entries(rewrites)) {
    const note = await db.xhsNote.findFirst({ where: { title: oldTitle } });
    if (!note) { console.log(`⚠️  找不到: ${oldTitle}`); continue; }

    await db.xhsNote.update({
      where: { id: note.id },
      data: {
        title: data.title,
        content: data.content,
        tags: data.tags,
      },
    });
    console.log(`✅ 更新: "${data.title}"`);
    updated++;
  }
  console.log(`\n🎉 共更新 ${updated} 篇`);
  await db.$disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
